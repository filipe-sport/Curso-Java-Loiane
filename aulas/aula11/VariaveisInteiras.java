public class VariaveisInteiras {
    public static void main(String[] args) {
        byte idade01 = 20;
        short idade02 = 21;
        int idade03 = 22;
        long idade04 = 23;
        System.out.println("Idade 01: " + idade01);    
        System.out.println("Idade 02: " + idade02);
        System.out.println("Idade 03: " + idade03);
        System.out.println("Idade 04: " + idade04);
    }
}
    
    
